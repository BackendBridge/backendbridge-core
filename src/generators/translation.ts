export function generateLaravelTranslations(contract: ApiContract, outPath: string): string[] {
  const resources = inferResources(contract);
  const generated: string[] = [];

  for (const locale of ["en", "fr"] as const) {
    const langDir = path.join(outPath, "lang", locale);
    ensureDir(langDir);

    const validationPath = path.join(langDir, "validation.php");
    fs.writeFileSync(validationPath, laravelValidationLangFile(locale), "utf8");
    generated.push(validationPath);

    for (const resource of resources) {
      const snake = toSnake(resource);
      const filePath = path.join(langDir, `${snake}.php`);
      fs.writeFileSync(filePath, laravelLangFile(resource, locale), "utf8");
      generated.push(filePath);
    }
  }

  return generated;
}

// ─── Symfony translations ─────────────────────────────────────────────────────

function symfonyMessagesYaml(resources: string[], locale: "en" | "fr"): string {
  const lines: string[] = [
    `# Symfony translations — ${locale} — generated by BackendBridge`,
    `# Place in translations/messages.${locale}.yaml`,
    "",
  ];

  const labels: Record<"en" | "fr", (r: string) => Record<string, string>> = {
    en: (r) => ({
      [`${toSnake(r)}.created`]: `${r} created successfully.`,
      [`${toSnake(r)}.updated`]: `${r} updated successfully.`,
      [`${toSnake(r)}.deleted`]: `${r} deleted successfully.`,
      [`${toSnake(r)}.not_found`]: `${r} not found.`,
      [`${toSnake(r)}.unauthorized`]: "Unauthorized.",
      [`${toSnake(r)}.forbidden`]: "Forbidden.",
    }),
    fr: (r) => ({
      [`${toSnake(r)}.created`]: `${r} créé(e) avec succès.`,
      [`${toSnake(r)}.updated`]: `${r} mis(e) à jour avec succès.`,
      [`${toSnake(r)}.deleted`]: `${r} supprimé(e) avec succès.`,
      [`${toSnake(r)}.not_found`]: `${r} introuvable.`,
      [`${toSnake(r)}.unauthorized`]: "Non authentifié.",
      [`${toSnake(r)}.forbidden`]: "Accès refusé.",
    }),
  };

  for (const resource of resources) {
    lines.push(`# ${resource}`);
    for (const [key, value] of Object.entries(labels[locale](resource))) {
      lines.push(`'${key}': '${value}'`);
    }
    lines.push("");
  }

  lines.push("# Validation");
  if (locale === "en") {
    lines.push("'error.validation': 'Validation failed.'");
    lines.push("'error.server': 'An unexpected error occurred.'");
    lines.push("'error.not_found': 'Resource not found.'");
  } else {
    lines.push("'error.validation': 'Validation échouée.'");
    lines.push("'error.server': 'Une erreur inattendue est survenue.'");
    lines.push("'error.not_found': 'Ressource introuvable.'");
  }

  return lines.join("\n") + "\n";
}

export function generateSymfonyTranslations(contract: ApiContract, outPath: string): string[] {
  const resources = inferResources(contract);
  const generated: string[] = [];
  const dir = path.join(outPath, "translations");
  ensureDir(dir);

  for (const locale of ["en", "fr"] as const) {
    const filePath = path.join(dir, `messages.${locale}.yaml`);
    fs.writeFileSync(filePath, symfonyMessagesYaml(resources, locale), "utf8");
    generated.push(filePath);
  }

  return generated;
}
