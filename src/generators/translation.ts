import fs from "node:fs";
import path from "node:path";
import type { ApiContract } from "../types.js";
import { toStudly, toSnake, ensureDir } from "../utils.js";

function inferResources(contract: ApiContract): string[] {
  const seen = new Set<string>();
  for (const ep of contract.endpoints) {
    const name = ep.tags?.[0] ? toStudly(ep.tags[0]) : toStudly(ep.operationId);
    if (name) seen.add(name);
  }
  return [...seen];
}

// ─── Laravel lang files ───────────────────────────────────────────────────────

function laravelLangFile(resource: string, locale: "en" | "fr"): string {
  const snake = toSnake(resource);
  const labels: Record<"en" | "fr", Record<string, string>> = {
    en: {
      singular: resource,
      plural: `${resource}s`,
      created: `${resource} created successfully.`,
      updated: `${resource} updated successfully.`,
      deleted: `${resource} deleted successfully.`,
      not_found: `${resource} not found.`,
      validation_failed: "Validation failed.",
      unauthorized: "Unauthorized.",
      forbidden: "Forbidden.",
    },
    fr: {
      singular: resource,
      plural: `${resource}s`,
      created: `${resource} créé(e) avec succès.`,
      updated: `${resource} mis(e) à jour avec succès.`,
      deleted: `${resource} supprimé(e) avec succès.`,
      not_found: `${resource} introuvable.`,
      validation_failed: "Validation échouée.",
      unauthorized: "Non authentifié.",
      forbidden: "Accès refusé.",
    },
  };

  const entries = Object.entries(labels[locale])
    .map(([k, v]) => `    '${k}' => '${v}',`)
    .join("\n");

  return `<?php

return [
${entries}
];
`;
}

